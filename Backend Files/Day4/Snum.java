
package Day4;
import java.util.Scanner;
public class Snum {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int n =sc.nextInt();
		for (int i=1;i<=n;i++) {
			System.out.print(i*i+" ");
		}
		sc.close();
	}
}
